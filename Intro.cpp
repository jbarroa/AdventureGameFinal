void getPlayerInfo(Player &p1, Player &p2, Player &p3) {
  string playerName;
  string schoolChoice;
  cout << "What is your player's name?" << endl;
  getline(cin, playerName);
  if(playerName[0] == 'X')
    playerName[0] = 'x';
  while(playerName[0] == ' ') {
    cout << "Invalid: Name cannot begin with a space. Enter a valid name: ";
    getline(cin, playerName);
    if(playerName[0] == 'X')
      playerName[0] = 'x';
  }
  p1.setPlayerName(playerName);
  
  do{
    cout << "\nChoose your player's school?" << endl;
    cout << "1. Hogwarts School." << endl;
    cout << "2. Beauxbatons Academy." << endl;
    cout << "3. Durmstrang Institute." << endl;
    cin >> schoolChoice;
    if(schoolChoice == "1"){
      p1.setSchool("Hogwarts School");
      p2.setPlayerName("Fleur Delacour");
      p2.setSchool("Beauxbatons Academy");
      p3.setPlayerName("Viktor Krum");
      p3.setSchool("Durmstrang Institute");
      system("clear");
    }else if(schoolChoice == "2"){
      p1.setSchool("Beauxbatons Academy");
      p2.setPlayerName("Cedric Diggory");
      p2.setSchool("Hogwarts School");
      p3.setPlayerName("Viktor Krum");
      p3.setSchool("Durmstrang Institute");
      system("clear");
    }else if(schoolChoice == "3"){
      p1.setSchool("Durmstrang Institute");
      p2.setPlayerName("Fleur Delacour");
      p2.setSchool("Beauxbatons Academy");
      p3.setPlayerName("Cedric Diggory");        
      p3.setSchool("Hogwarts School");
      system("clear");
    }else{
      cout << "Please enter 1-3 to choose your school." << endl;
      schoolChoice = "0";
    }
  }while(schoolChoice == "0");


}